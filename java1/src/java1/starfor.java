package java1;

public class starfor {
	public static void main(String[] args)
	{
		
		for(int b =10 ; b>=1; b--)
		{
	
		for(int a =1 ; a <=b; a++)
		{
			System.out.print("*");
		
		}
		System.out.println();
		}
		
		for(int b =2 ; b<=10; b++)
		{
	
		for(int a =1 ; a <=b; a++)
		{
			System.out.print("*");
		
		}
		System.out.println();
		}
		
		
	}	

	}


