package java1;

public class jonstar {
	public static void main(String[] args) {
		int line = 6;
	
		for(int a = 1 ; a<=line ; a++)
		{
			for(int b =(line-1) ; b>=a; b-- )
			{
				System.out.print(" ");
				
			}
			for(int b = 1 ; b<=a ; b++)
			{
				System.out.print("* ");
			}
		System.out.println();	
	}
		
		
		
		
		for(int a =5 ; a>=1 ; a--)
		{
			for(int b =4; b>=a; b-- )
			{
				System.out.print(" ");
				
			}
			for(int b = 1 ; b<=a ; b++)
			{
				System.out.print(" *");
			}
		System.out.println();	
	}


}
}
